function encriptar() {
  let texto = document.getElementById("texto").value;
  let tituloMensaje = document.getElementById("titulo-mensaje");
  let parrafo = document.getElementById("parrafo");

  let textoCifrado = texto
    .replace(/e/gi, "enter")
    .replace(/i/gi, "imes")
    .replace(/a/gi, "ai")
    .replace(/o/gi, "ober")
    .replace(/u/gi, "ufat");

  if (texto.length != 0) {
    document.getElementById("texto").value = textoCifrado;
    tituloMensaje.textContent = "felicidad, el texto hacido encriptado ";
    parrafo.textContent = "";
  } else {
    tituloMensaje.textContent = "no se a encontrado ningun texto";
    parrafo.textContent = " por favor Ingresa el texto que deseas encriptar o desencriptar";
    swal("error!", "vuelva intentalo de nuevo", "warning");
  }
}

